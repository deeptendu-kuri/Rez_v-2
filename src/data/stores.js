export const getStoreById = (id) => stores.find(s => s.id === parseInt(id));

export const filterStores = (stores, filters) => {
  return stores.filter(store => {
    if (filters.halal && !store.isHalal) return false;
    if (filters.vegan && !store.isVegan) return false;
    if (filters.vegetarian && !store.isVegetarian) return false;
    if (filters.sawan && !store.isSawan) return false;
    if (filters.is60Min && !store.is60Min) return false;
    if (filters.category && store.category !== filters.category) return false;
    return true;
  });
};
